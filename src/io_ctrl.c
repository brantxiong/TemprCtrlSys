#include "..\\head\\common.h"
#include "..\\head\\io_ctrl.h"

/***************************************************************
* 功能：初始化相应端口输出P0.1,P0.2设置为输出
***************************************************************/
void IOInit()
{
	GP0DAT |= (1<<25);
	GP0DAT |= (1<<26);
}

/***************************************************************
* 功能：P0.1置位
***************************************************************/
void BeepOn()
{
	GP0SET |= (1<<17);
}

/***************************************************************
* 功能：P0.1清零
***************************************************************/
void BeepOff()
{
	GP0CLR |= (1<<17);
}

/***************************************************************
* 功能：P0.2置位
***************************************************************/
void RelayOn()
{
	GP0CLR |= (1<<18);
}

/***************************************************************
* 功能：P0.2清零
***************************************************************/
void RelayOff()
{
	GP0SET |= (1<<18);
}
